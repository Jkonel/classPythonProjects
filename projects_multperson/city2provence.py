'''
@description: 城市名提取函数
@param : city_name:输入原始字符串
@return: 处理后的字符串，只包含城市名(example: '上海市')
'''
def city_get(stri):
    res = re.findall(r'^[\u4e00-\u9fa5]+', stri)
    if res:
        if '异地' in res[0]:
            res = None
        else:
            res = res[0]+'市'
    else:
        res = None
    return res



